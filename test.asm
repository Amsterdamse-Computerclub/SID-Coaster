	sta $d011
	lda #irq_line_top
	sta $d012
	lda #$01
	sta $d01a
	// init timers
	lda #$7f
	sta $dc0d
	sta $dd0d
	lda $dc0d
	lda $dd0d
	// purge pending interrupts
	asl $d019
	cli

	lda $d018
	sta screen

	lda #%10000101
	sta $d018

	jmp *

irq_top:
	pha
	txa
	pha
	tya
	pha

	inc $d020
	jsr music.play
	dec $d020

	// demp de percussie als de joystick ingedrukt is
	lda $dc00
	and #%10000
	bne !l+

	inc screen
	lda #0
	sta $d412

!l:

	jsr col_scroll

	pla
	tay
	pla
	tax
	pla
irq_dummy:
	asl $d019
	rti

col_scroll:
	// push new color at end
	ldx col_index
	lda colors, x
	sta colram + line_offset + 40 - 1

	// move left
	ldx #0
	ldy #40
!l:
	lda colram + line_offset + 1, x
	sta colram + line_offset, x
	inx
	dey
	bne !l-

	// col_index = (col_index + 1) % 32
	inc col_index
	lda col_index
	and #31
	sta col_index

	rts

